# 11. Write a python script to implement Complex Number Operations

a = complex(2, 3)
b = complex(4, 5)

print("a = ", a)
print("b = ", b)

print("a + b = ", a + b)
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = {:.2f}".format(a / b))
print("Conjugate of a = ", a.conjugate())
print("Magnitude of a = ", abs(a))
